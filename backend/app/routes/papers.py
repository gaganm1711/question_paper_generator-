@router.post("/", response_model=PaperOut, status_code=status.HTTP_201_CREATED)
def generate_ai_paper(
    req: PaperGenerateRequest, 
    db: Session = Depends(get_db), 
    current_user: User = Depends(get_current_user)
):
    """Uses pgvector semantic retrieval and Google Gemini Flash to generate a balanced multi-set exam paper."""
    # 1. Fetch academic names
    board = db.query(Board).filter(Board.id == req.board_id).first()
    classroom = db.query(Class).filter(Class.id == req.class_id).first()
    subject = db.query(Subject).filter(Subject.id == req.subject_id).first()
    chapters = db.query(Chapter).filter(Chapter.id.in_(req.chapter_ids)).all()
    
    if not (board and classroom and subject and chapters):
        raise HTTPException(
            status_code=400, 
            detail="Invalid board, class, subject, or chapter selection."
        )

    chapter_names = [ch.chapter_name for ch in chapters]

    # 2. Call Gemini API to generate paper
    total_marks = req.total_marks
    if req.question_breakdown:
        total_marks = sum(int(m) * count for m, count in req.question_breakdown.items() if count > 0)

    difficulty_dist = {
        "easy": req.easy_percent,
        "medium": req.medium_percent,
        "hard": req.hard_percent
    }
    
    generated_json = gemini_service.generate_question_paper(
        board_name=board.board_name,
        class_name=classroom.class_name,
        subject_name=subject.subject_name,
        chapters=chapter_names,
        total_marks=total_marks,
        difficulty_dist=difficulty_dist,
        language=req.language,
        num_sets=req.num_sets,
        time_allowed=req.time_allowed_minutes,
        school_name=req.school_name,
        question_breakdown=req.question_breakdown,
        num_diagram_questions=req.num_diagram_questions
    )

    # 4. Save to Database
    paper_id = str(uuid.uuid4())
    paper_name = f"{classroom.class_name} {subject.subject_name} ({total_marks} Marks)"
    
    # Inject school name and metadata into JSON for rendering
    generated_json["school_name"] = req.school_name
    generated_json["board"] = board.board_name
    generated_json["class_name"] = classroom.class_name
    generated_json["subject"] = subject.subject_name
    generated_json["total_marks"] = total_marks
    generated_json["time_allowed_minutes"] = req.time_allowed_minutes
    generated_json["date"] = req.date if req.date else "29/12/24"
    if req.instructions is not None:
        generated_json["instructions"] = req.instructions
    if req.constants is not None:
        generated_json["constants"] = req.constants

    db_paper = GeneratedPaper(
        id=paper_id,
        paper_name=paper_name,
        paper_json=generated_json,
        created_by=current_user.id
    )
    
    db.add(db_paper)
    db.commit()
    db.refresh(db_paper)
    
    return db_paper

